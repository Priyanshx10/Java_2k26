package Variable;

public class Variable {
    public static void main(String[] args) {
        int age = 25;
        String name = "Priyansh";
        long marks = 90L;
        float percentage = 90.0f;
        
        System.out.println(name + " is " + age + " years old and scored " + marks + " marks which is equivalent to " + percentage + "%.");
    }
}
